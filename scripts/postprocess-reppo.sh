#!/usr/bin/env bash
# Post-process Reppo write intents left by Claude (sandbox blocks outbound network).
# Reads .pending-reppo/*.json, runs a --dry-run preflight, then the real write.
# Appends results to .outputs/reppo-trading-agent.md for the digest step.
#
# Env:
#   REPPO_PRIVATE_KEY     required for any write (script skips writes if unset)
#   REPPO_DRY_RUN_ONLY    if "true", run the dry-run preflight but skip real writes
set -euo pipefail

PENDING_DIR=".pending-reppo"
RESULTS_FILE=".outputs/reppo-trading-agent.md"
DRY_RUN_ONLY="${REPPO_DRY_RUN_ONLY:-false}"

if [ ! -d "$PENDING_DIR" ] || [ -z "$(ls -A "$PENDING_DIR"/*.json 2>/dev/null || true)" ]; then
  echo "reppo-postprocess: no pending intents"
  exit 0
fi

if [ -z "${REPPO_PRIVATE_KEY:-}" ]; then
  echo "reppo-postprocess: REPPO_PRIVATE_KEY not set, skipping all writes"
  mkdir -p .outputs
  {
    echo ""
    echo "## Execution Results"
    echo ""
    echo "_postprocess-reppo.sh skipped ALL writes: REPPO_PRIVATE_KEY is not set. No intents were executed on-chain._"
  } >> ".outputs/reppo-trading-agent.md"
  exit 0
fi

if ! command -v reppo >/dev/null 2>&1; then
  npm i -g @reppo/cli >/dev/null 2>&1 || {
    echo "reppo-postprocess: CLI missing"
    mkdir -p .outputs
    {
      echo ""
      echo "## Execution Results"
      echo ""
      echo "_postprocess-reppo.sh could not run: Reppo CLI unavailable. Pending intents were NOT executed._"
    } >> ".outputs/reppo-trading-agent.md"
    exit 0
  }
fi

mkdir -p .outputs .reppo-cache
{
  echo ""
  echo "## Execution Results"
  echo ""
  echo "_Generated by postprocess-reppo.sh ($(date -u +%FT%TZ)). dry_run_only=${DRY_RUN_ONLY}_"
  echo ""
} >> "$RESULTS_FILE"

# Build the CLI argument list for an intent file. Args: file. Echoes args.
# Returns 1 if the command is unknown or any field is malformed. Validating
# fields here keeps the later unquoted `$args` expansion safe (every word is
# then a controlled flag, an id token, or a positive integer — no spaces).
# `id` accepts Reppo's integer datanet/pod ids as well as hex/hyphenated ids.
build_args() {
  local f="$1" cmd id='^[A-Za-z0-9_-]+$'
  cmd="$(jq -r '.cmd' "$f")"
  case "$cmd" in
    mint-pod)
      local datanet
      datanet="$(jq -r '.datanet' "$f")"
      [[ "$datanet" =~ $id ]] || return 1
      printf 'mint-pod --datanet %s' "$datanet" ;;
    vote)
      local pod dir votes flag
      pod="$(jq -r '.pod' "$f")"
      dir="$(jq -r '.direction' "$f")"
      votes="$(jq -r '.votes // 1' "$f")"
      [[ "$pod" =~ $id ]] || return 1
      [[ "$votes" =~ ^[1-9][0-9]*$ ]] || return 1
      case "$dir" in
        like) flag="--like" ;;
        dislike) flag="--dislike" ;;
        *) return 1 ;;
      esac
      printf 'vote --pod %s --votes %s %s' "$pod" "$votes" "$flag" ;;
    *) return 1 ;;
  esac
}

# Helper: extract a structured error code from a reppo CLI JSON failure response.
# Returns .error.code (Reppo's standard error shape), or .code (legacy), or "UNKNOWN".
extract_code() {
  jq -r '.error.code // .code // "UNKNOWN"' "$1" 2>/dev/null || echo UNKNOWN
}

# Helper: extract a single-line, <=300-char detail from a captured CLI output file.
# Whitespace runs collapse to a single space so the result fits on one Markdown bullet.
extract_detail() {
  tr -s '[:space:]' ' ' < "$1" 2>/dev/null | cut -c1-300 || true
}

# Helper: run a dry-run for the current intent. Retries up to 3 attempts on
# transient RPC failures (INTERNAL_ERROR / TIMEOUT / RATE_LIMITED /
# NETWORK_ERROR) with linear backoff. Relies on caller-scope $args / $key /
# $base. Returns 0 on success, 1 on terminal failure (last attempt's code
# remains in .reppo-cache/dryrun-$base for the caller to inspect).
dryrun_with_retry() {
  local attempt=1 code wait
  while : ; do
    if REPPO_NETWORK=mainnet reppo $args --idempotency-key "$key" --dry-run --json \
         > ".reppo-cache/dryrun-$base" 2>&1; then
      return 0
    fi
    code="$(extract_code ".reppo-cache/dryrun-$base")"
    case "$code" in
      INTERNAL_ERROR|TIMEOUT|RATE_LIMITED|NETWORK_ERROR)
        if [ "$attempt" -ge 3 ]; then return 1; fi
        wait=$((attempt * 5))
        echo "reppo-postprocess: transient $code on $base, retry in ${wait}s (attempt $attempt/3)" >&2
        sleep "$wait"
        attempt=$((attempt + 1))
        ;;
      *) return 1 ;;
    esac
  done
}

# Helper: ensure foundry's `cast` is available. Used by the auto-approve path
# below (the Reppo CLI does not yet ship its own ERC20 approve helper). Installs
# foundry on first use; cached for subsequent calls within the same runner.
ensure_cast() {
  # Fast path: in CI the workflow's "Install Foundry" step
  # (foundry-rs/foundry-toolchain) puts cast on PATH before this runs.
  command -v cast >/dev/null 2>&1 && return 0

  # Fallback for ./aeon local runs or if the workflow step ever fails.
  # Logs visibly so a future install failure is debuggable.
  echo "reppo-postprocess: cast not on PATH; installing foundry inline..." >&2
  local log=".reppo-cache/foundry-install.log"
  : > "$log"
  if ! curl -fsSL https://foundry.paradigm.xyz 2>>"$log" | bash >>"$log" 2>&1; then
    echo "reppo-postprocess: foundry init script failed — log tail:" >&2
    tail -5 "$log" 2>/dev/null >&2 || true
    return 1
  fi
  export PATH="$HOME/.foundry/bin:$PATH"
  if ! "$HOME/.foundry/bin/foundryup" >>"$log" 2>&1; then
    echo "reppo-postprocess: foundryup failed — log tail:" >&2
    tail -5 "$log" 2>/dev/null >&2 || true
    return 1
  fi
  command -v cast >/dev/null 2>&1
}

# Helper: resolve the REPPO ERC20 token address. Prefers the operator-set
# REPPO_TOKEN_ADDRESS env (cheapest, no on-chain call). Falls back to
# discovering it from the SubnetManager via a few common ABI getters.
# Echoes the address on success, empty on failure.
resolve_reppo_token() {
  local spender="$1" addr
  if [ -n "${REPPO_TOKEN_ADDRESS:-}" ]; then
    echo "$REPPO_TOKEN_ADDRESS"
    return 0
  fi
  if command -v cast >/dev/null 2>&1 && [ -n "${REPPO_RPC_URL:-}" ]; then
    for sig in "feeToken()(address)" "REPPO()(address)" "paymentToken()(address)" "token()(address)"; do
      addr="$(cast call "$spender" "$sig" --rpc-url "$REPPO_RPC_URL" 2>/dev/null | head -1 || true)"
      if [[ "$addr" =~ ^0x[0-9a-fA-F]{40}$ ]] && [ "$addr" != "0x0000000000000000000000000000000000000000" ]; then
        echo "$addr"
        return 0
      fi
    done
  fi
  echo ""
  return 1
}

# Helper: try grant-access. On INSUFFICIENT_ALLOWANCE, auto-approve REPPO
# spend to the SubnetManager (cast send approve(...)) and retry grant-access.
# Returns 0 on grant success, 1 on terminal failure (final grant response
# remains in .reppo-cache/grant-$target.json for the caller to inspect).
auto_recover_grant() {
  local target="$1" gcode spender reppo_token approve_tx adetail
  if REPPO_NETWORK=mainnet reppo grant-access --datanet "$target" --json \
       > ".reppo-cache/grant-$target.json" 2>&1; then
    return 0
  fi
  gcode="$(extract_code ".reppo-cache/grant-$target.json")"
  if [ "$gcode" != "INSUFFICIENT_ALLOWANCE" ]; then
    return 1  # not a recoverable failure; caller reports it
  fi

  # Extract the spender (SubnetManager) from the error hint —
  # "Approve the SubnetManager (0x...)".
  spender="$(grep -oE 'SubnetManager \(0x[0-9a-fA-F]{40}\)' ".reppo-cache/grant-$target.json" \
              | grep -oE '0x[0-9a-fA-F]{40}' | head -1)"
  if [ -z "$spender" ]; then
    echo "  - auto-approve aborted: SubnetManager address not found in grant error" >> "$RESULTS_FILE"
    return 1
  fi

  if ! ensure_cast; then
    echo "  - auto-approve aborted: foundry/cast install failed" >> "$RESULTS_FILE"
    return 1
  fi

  reppo_token="$(resolve_reppo_token "$spender")"
  if [ -z "$reppo_token" ]; then
    echo "  - auto-approve aborted: REPPO token address unknown — set REPPO_TOKEN_ADDRESS or ensure SubnetManager exposes feeToken()/REPPO()/paymentToken()/token()" >> "$RESULTS_FILE"
    return 1
  fi

  echo "reppo-postprocess: approving SubnetManager $spender for REPPO ($reppo_token)..." >&2
  # MAX_UINT256 approval — one-time, never re-approve. The SubnetManager
  # contract is the only counterparty that can move REPPO from this account
  # via this allowance; operator trust in that contract is implicit anyway
  # (they're already using it for grant-access).
  if ! cast send "$reppo_token" "approve(address,uint256)" "$spender" \
       0xffffffffffffffffffffffffffffffffffffffffffffffffffffffffffffffff \
       --rpc-url "${REPPO_RPC_URL:-https://mainnet.base.org}" \
       --private-key "$REPPO_PRIVATE_KEY" --json \
       > ".reppo-cache/approve-$target.json" 2>&1; then
    adetail="$(extract_detail ".reppo-cache/approve-$target.json")"
    echo "  - auto-approve FAILED: ${adetail:-<empty>}" >> "$RESULTS_FILE"
    return 1
  fi
  approve_tx="$(jq -r '.transactionHash // .txHash // "n/a"' ".reppo-cache/approve-$target.json" 2>/dev/null || echo n/a)"
  echo "  - auto-approved REPPO spend to SubnetManager $spender (tx: $approve_tx)" >> "$RESULTS_FILE"

  # Retry grant-access now that allowance is set.
  if REPPO_NETWORK=mainnet reppo grant-access --datanet "$target" --json \
       > ".reppo-cache/grant-$target.json" 2>&1; then
    return 0
  fi
  return 1
}

for intent in "$PENDING_DIR"/*.json; do
  [ -f "$intent" ] || continue
  base="$(basename "$intent")"
  key="$(jq -r '.idempotency_key // empty' "$intent")"
  if [ -z "$key" ]; then
    echo "- \`$base\` — **skipped**: missing idempotency_key" >> "$RESULTS_FILE"
    rm -f "$intent"
    continue
  fi
  args="$(build_args "$intent")" || {
    echo "- \`$base\` — **skipped**: unknown command or invalid fields" >> "$RESULTS_FILE"
    rm -f "$intent"
    continue
  }

  # Dry-run preflight. On a mint-pod failure with PUBLISHER_LACKS_SUBNET_ACCESS,
  # auto-grant datanet access and retry once. Gated on DRY_RUN_ONLY because
  # grant-access costs a real fee — Phase 0 stays free, real mode self-heals.
  echo "reppo-postprocess: dry-run $base..."
  if ! dryrun_with_retry; then
    code="$(extract_code ".reppo-cache/dryrun-$base")"
    detail="$(extract_detail ".reppo-cache/dryrun-$base")"
    cmd_name="$(jq -r '.cmd' "$intent")"

    if [ "$code" = "PUBLISHER_LACKS_SUBNET_ACCESS" ] && [ "$cmd_name" = "mint-pod" ] && [ "$DRY_RUN_ONLY" != "true" ]; then
      target="$(jq -r '.datanet' "$intent")"
      echo "reppo-postprocess: $base needs datanet access; auto-granting datanet $target..." >&2
      if auto_recover_grant "$target"; then
        grant_tx="$(jq -r '.txHash // .transactionHash // "n/a"' ".reppo-cache/grant-$target.json" 2>/dev/null || echo n/a)"
        echo "  - auto-granted datanet $target access (tx: $grant_tx)" >> "$RESULTS_FILE"
        # Retry the dry-run now that access is granted.
        if ! dryrun_with_retry; then
          code="$(extract_code ".reppo-cache/dryrun-$base")"
          detail="$(extract_detail ".reppo-cache/dryrun-$base")"
          echo "- \`$base\` — **dry-run failed after grant** (code: $code), real write skipped" >> "$RESULTS_FILE"
          echo "  - output: ${detail:-<empty>}" >> "$RESULTS_FILE"
          rm -f "$intent"
          continue
        fi
        # Retry passed — fall through to the dry-run-OK / real-write logic below.
      else
        gcode="$(extract_code ".reppo-cache/grant-$target.json")"
        gdetail="$(extract_detail ".reppo-cache/grant-$target.json")"
        echo "- \`$base\` — **dry-run failed** (code: $code) and auto-grant for datanet $target failed (code: $gcode), real write skipped" >> "$RESULTS_FILE"
        echo "  - output: ${detail:-<empty>}" >> "$RESULTS_FILE"
        echo "  - grant output: ${gdetail:-<empty>}" >> "$RESULTS_FILE"
        rm -f "$intent"
        continue
      fi
    else
      echo "- \`$base\` — **dry-run failed** (code: $code), real write skipped" >> "$RESULTS_FILE"
      echo "  - output: ${detail:-<empty>}" >> "$RESULTS_FILE"
      echo "reppo-postprocess: dry-run failed for $base: ${detail:-<empty>}" >&2
      rm -f "$intent"
      continue
    fi
  fi

  if [ "$DRY_RUN_ONLY" = "true" ]; then
    echo "- \`$base\` — dry-run OK, real write skipped (REPPO_DRY_RUN_ONLY)" >> "$RESULTS_FILE"
    rm -f "$intent"
    continue
  fi

  # Real write.
  echo "reppo-postprocess: executing $base..."
  if REPPO_NETWORK=mainnet reppo $args --idempotency-key "$key" --json \
     > ".reppo-cache/result-$base" 2>&1; then
    tx="$(jq -r '.txHash // .transactionHash // "n/a"' ".reppo-cache/result-$base" 2>/dev/null || echo n/a)"
    echo "- \`$base\` — **success** (tx: $tx)" >> "$RESULTS_FILE"
  else
    code="$(extract_code ".reppo-cache/result-$base")"
    detail="$(extract_detail ".reppo-cache/result-$base")"
    echo "- \`$base\` — **write failed** (code: $code)" >> "$RESULTS_FILE"
    echo "  - output: ${detail:-<empty>}" >> "$RESULTS_FILE"
    echo "reppo-postprocess: write failed for $base: ${detail:-<empty>}" >&2
  fi
  rm -f "$intent"
done

echo "reppo-postprocess: done"
